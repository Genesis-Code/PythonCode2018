# Addition (Uses '+')
print(1+2)

# Subtraction (Uses '-')
print(2 - 1)

# Multiplication (Uses '*')
print(3 * 2)

# Division (Uses '/')
print(6/2)

# Integer Division (Uses '//')
print(5//2)

# Modulus (remainder) (Uses '%')
print(5 % 3)
print(7 % 2)

# Exponents (repeated multiplication) (Uses '**')
print(3**2)
